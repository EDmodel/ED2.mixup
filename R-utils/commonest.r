#==========================================================================================#
#     This function finds the commonest value for a given vector.                          #
#------------------------------------------------------------------------------------------#
commonest = function(x,na.rm=FALSE) {
  if (na.rm) x = x[! is.na(x)]

  unique.x = unique(x)
  often    = unique.x[which.max(tabulate(match(x, unique.x)))]
  return(often)
}#end function
