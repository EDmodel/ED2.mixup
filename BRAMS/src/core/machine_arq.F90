!==========================================================================================!
!==========================================================================================!
!      Module necessary to set the best parameters to IA32 or SX-6 machine.                !
!------------------------------------------------------------------------------------------!
module machine_arq

  implicit none
  !----------------------------------------------------------------------------------------!
  !  Where:                                                                                !
  !  machine=0 => Generic machine (aka anything but SX-6).                                 !
  !  machine=1 => SX-6 Vetorial machine                                                    !
  !----------------------------------------------------------------------------------------!

#if defined(NEC_SX)
   integer, parameter :: machine=1
#else
   integer, parameter :: machine=0
#endif

end module machine_arq
